package com.idevebi.algorithms.datastructures.linkedList;

/**
 * 链表节点
 */
public class ListNode {

    /**
     * 节点值
     */
    int val;
    /**
     * next 指针
     */
    ListNode next;

    /**
     * 构造器
     *
     * @param val 节点值
     */
    ListNode(int val) {
        this.val = val;
    }
}
